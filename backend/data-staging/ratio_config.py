# -*- coding: utf-8 -*-
"""계열별 표준 반영비율 추정치 — extract/build 스크립트 공유 단일 출처.
   모집요강 부재로 인한 계열 추정치이며 0단계 위치 스크리닝용(합불 정밀예측 아님).
   NAT/HUM은 두 스크립트의 과거 분기(drift)를 합집합으로 통합: NAT에 '의학' 포함, HUM에 예체능 포함."""

NAT = ['공학','공과','컴퓨터','기계','전자','전기','화학','화공','물리','수학','의과','의예','의학','약','간호',
       '생명','소재','건축','통계','데이터','인공지능','반도체','에너지','바이오','정보','소프트','보건','식품',
       '지구','환경','수의','치','보안','모빌리티','항공','해양','농','산림','과학']

HUM = ['국어','국문','영어','영문','사학','철학','경영','경제','사회','정치','행정','교육','심리','미디어','법',
       '문헌','문예','문화','관광','복지','신학','상담','어문','문학','예술','디자인','음악','미술','체육','무용',
       '연극','글로벌','국제','자유전공','인문','경찰','세무','회계','무역','금융']

def ratios(d):
    """(국,수,영,탐) 계열 표준 추정치."""
    if any(k in d for k in NAT): return (0.20, 0.35, 0.15, 0.30)
    if any(k in d for k in HUM): return (0.30, 0.25, 0.20, 0.25)
    return (0.25, 0.30, 0.20, 0.25)
